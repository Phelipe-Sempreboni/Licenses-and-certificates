UPDATE [SUCOS_VENDAS].[DBO].[TABELA DE PRODUTOS] SET [PREÇO DE LISTA] = 7
WHERE [CODIGO DO PRODUTO] = '1000889'

SELECT 
	B.[CODIGO DO PRODUTO], 
	B.[PREÇO DE LISTA], 
	A.[CODIGO], 
	A.[PREÇO LISTA]

FROM [VENDAS_SUCOS].[DBO].[PRODUTOS] AS A 
INNER JOIN [SUCOS_VENDAS].[DBO].[TABELA DE PRODUTOS] AS B ON A.[CODIGO] = B.[CODIGO DO PRODUTO]
WHERE A.[CODIGO] = '1000889'

UPDATE A SET A.[PREÇO LISTA] = B.[PREÇO DE LISTA]
FROM [VENDAS_SUCOS].[DBO].[PRODUTOS] AS A 
INNER JOIN [SUCOS_VENDAS].[DBO].[TABELA DE PRODUTOS] AS B ON A.[CODIGO] = B.[CODIGO DO PRODUTO]
WHERE A.[CODIGO] = '1000889'

