	 A.CPF
	,A.[VOLUME DE COMPRA]
FROM [VENDAS_SUCOS].[DBO].[CLIENTES] AS A
INNER JOIN [VENDAS_SUCOS].[DBO].[VENDEDORES] AS B ON A.[BAIRRO] = B.[BAIRRO]

